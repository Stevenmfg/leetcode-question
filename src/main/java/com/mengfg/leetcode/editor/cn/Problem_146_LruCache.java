    static class LRUCache {
        int size;
        int capacity;
        Node head;
        Node tail;
        Map<Integer, Node> map;

        public LRUCache(int capacity) {
            this.capacity = capacity;
            size = 0;
            map = new HashMap<>(capacity);
            head = new Node();
            tail = new Node();
            head.next = tail;
            tail.prev = head;
        }

        public int get(int key) {
            if (!map.containsKey(key)) {
                return -1;
            }

            Node newHead = map.get(key);
            moveToHead(newHead);
            return newHead.value;
        }

        public void put(int key, int value) {

            if (size == 0) {
                Node node = new Node(key, value);
                addToHead(node);
                map.put(key, node);
                size++;
                return;
            }

            // åŒ…å«keyï¼Œä¿®æ”¹å€¼å¹¶æ·»åŠ åˆ°é˜Ÿåˆ—é¦–éƒ¨
            if (map.containsKey(key)) {
                map.get(key).value = value;
                moveToHead(map.get(key));
                return;
            }

            // ä¸åŒ…å«keyï¼Œæ·»åŠ ä¸­åˆ°é¦–éƒ¨ï¼Œsize++
            Node node = new Node(key, value);
            addToHead(node);
            map.put(key, node);
            size++;

            // é˜Ÿåˆ—å·²æ»¡ï¼Œåˆ é™¤æœ€åä¸€ä¸ª
            if (size > capacity) {
                map.remove(tail.prev.key);
                removeNode(tail.prev);
                size--;
            }
        }

        private void addToHead(Node node) {
            Node next = head.next;
            next.prev = node;
            node.next = next;
            node.prev = head;
            head.next = node;
        }

        private void moveToHead(Node node) {
            removeNode(node);
            addToHead(node);
        }

        private void removeNode(Node node) {
            Node prev = node.prev;
            Node next = node.next;
            prev.next = next;
            next.prev = prev;
        }

    }

    static class Node {
        int key;
        int value;
        Node prev;
        Node next;

        public Node() {
        }

        public Node(int key, int value) {
            this.key = key;
            this.value = value;
        }
    }

